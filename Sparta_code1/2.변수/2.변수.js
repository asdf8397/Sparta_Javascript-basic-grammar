// 변수
// let은 변수를 한번 할당하면 재할당 가능
// const는 변수를 한번 할당하면 재할당 할 수 없음
let my_name = "Kim Dongyoung"; // 세미콜론은 끝났다고 마침표라고 생각하면 됨
console.log(my_name);

my_name = "ben";
console.log(my_name);

const new_Name = "Kim_king";
console.log(new_Name);

new_Name = "My Name";

console.log(new_Name); // const는 재할당 할 수 없으므로 재할당 했을때 오류 발생