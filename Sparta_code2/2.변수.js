// 2. 변수_복습구간입니다.

// 변수선언을 할 수 있지만 let은 재선언 가능함
// 변수를 때때로 재할당 해야할 수 도 있음 그럴땐 let 사용
let my_name = "Mr.Kim";
console.log(my_name);

my_name = "ben";
console.log(my_name);

// 변수선언에서 const는 재할당이 불가능하다.
// 불변하지 않는 변수선언을 최초로 할당해서 사용할때 const 사용
const we_ara_champion = "height: 180cm";
console.log(we_ara_champion);
