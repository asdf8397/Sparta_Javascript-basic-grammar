// while, for문 - for문 사용

/* 20도인 온도를 25도까지 상승시켜라 */
for(temperature = 20; temperature <= 25; temperature++) {
    console.log(temperature)
};

/* 오늘 낮 온도는 34도 입니다. 현재 온도는 17도 입니다. 
기온상승을 나타내주세요 */

for (temperature2 = 17; temperature2 <= 34; temperature2++) {
    console.log(`현재 온도는 ${temperature2}도 입니다. 급격한
        기온상으로 자외선과 더위에 조심하세요`)
}